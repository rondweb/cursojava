
public class Matematica {

	public static void main(String[] args) {
		double A = Math.sqrt(25); //raiz quadrada
		double B = Math.pow(5, 3); //potencia
		double C = Math.abs(-5); //valor absoluto
		
		System.out.println("Raiz quadrada " + A);
		System.out.println("Potencia de 5 elevado a 3 " + B);
		System.out.println("Valor absoluto de -5 "  + C);

	}

}
